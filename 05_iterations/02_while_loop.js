let index = 0
while(index<=10)
{
    console.log(`Value of i = ${index}`);
    index = index+2
}

let myArray = ['flash','batman','superman','spiderman']

let arr = 0
while(arr<myArray.length){
    console.log(`Value is ${myArray[arr]}`)
    arr++
}

let score = 11
do{
    console.log(`Score is ${score}`);
    score++
}while(score<=10);

